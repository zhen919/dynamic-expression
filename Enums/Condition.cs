namespace DynamicExpression.Enums;

public enum Condition
{
    /// <summary>
    /// 等于
    /// </summary>
    Equals = 1,

    /// <summary>
    /// 不等于
    /// </summary>
    NotEquals = 2,

    /// <summary>
    /// 包含
    /// </summary>
    Contains = 3,

    /// <summary>
    /// 不包含
    /// </summary>
    NotContains = 4,

    /// <summary>
    /// 大于
    /// </summary>
    GreaterThan = 5,

    /// <summary>
    /// 小于
    /// </summary>
    LessThan = 6,

    /// <summary>
    /// 介于
    /// </summary>
    Between = 7,

    /// <summary>
    /// 大于或等于
    /// </summary>
    GreaterThanOrEquals = 8,

    /// <summary>
    /// 小于或等于
    /// </summary>
    LessThanOrEquals = 9,

}